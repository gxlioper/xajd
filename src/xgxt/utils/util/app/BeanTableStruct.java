package xgxt.utils.util.app;

public class BeanTableStruct {
	String cName;

	String cLable;

	String cTypeName;

	String cClassName;

	int cDisplaySize;

	String cType;

	public String getCClassName() {
		return cClassName;
	}

	public void setCClassName(String className) {
		cClassName = className;
	}

	public int getCDisplaySize() {
		return cDisplaySize;
	}

	public void setCDisplaySize(int displaySize) {
		cDisplaySize = displaySize;
	}

	public String getCLable() {
		return cLable;
	}

	public void setCLable(String lable) {
		cLable = lable;
	}

	public String getCName() {
		return cName;
	}

	public void setCName(String name) {
		cName = name;
	}

	public String getCType() {
		return cType;
	}

	public void setCType(String type) {
		cType = type;
	}

	public String getCTypeName() {
		return cTypeName;
	}

	public void setCTypeName(String typeName) {
		cTypeName = typeName;
	}

}
